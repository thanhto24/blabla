#include <bits/stdc++.h>

using namespace std;

long long n, X, Y, x, y, r, res = 1e18 + 1;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cin >> n >> X >> Y;
    for(int i = 1; i <= n; i++)
    {
        cin >> x >> y >> r;
        if((long long)(sqrt((X-x)*(X-x)+(Y-y)*(Y-y)) - r)<res)
        {
            res = (long long)(sqrt((X-x)*(X-x)+(Y-y)*(Y-y)) - r);
            if((long long)(sqrt((X-x)*(X-x)+(Y-y)*(Y-y)) - r)==(sqrt((X-x)*(X-x)+(Y-y)*(Y-y)) - r)) res --;
        }
    }
    cout << res;
    return 0;
}
