#include <bits/stdc++.h>
#define ll long long

using namespace std;

const int maxN = 1e9 + 7;

int q, dem;
ll res;
bool ok;
string s1, s2;

ll calc(int n)
{
    if(!n) return 1; 
    ll k = calc(n/2);
    if(n&1) return (k * k * 2)%maxN;
    else return (k * k)%maxN;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cin >> q;
    while(q--)
    {
        cin >> s1 >> s2;
        ok = true;
        dem = 0;
        for(int i = 0; i < s1.size(); i++)
        {
            if(s1[i]==s2[i])
            {
                if(s1[i]=='O')
                    ok = false;
                else
                    dem++;
            }
        }
        if(!ok) cout << 0 << endl;
        else
        {
            res = calc(dem);
            cout << res << endl;
        }
    }
    return 0;
}