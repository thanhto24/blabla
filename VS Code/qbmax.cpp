#include <bits/stdc++.h>

using namespace std;

int m, n, a[102][102], dp[102][102], ans = INT_MIN;

void enter()
{
    cin >> m >> n;
    for(int i = 1; i <= m; i++)
    {
        for(int j = 1; j <= n; j++)
            cin >> a[i][j];
    }
}

void process()
{
    for(int i = 0; i <= n; i++)
        a[0][i] = a[m+1][i] = 0;

    for(int j = 1; j <= n; j++)
    {
        for(int i = 1; i <= m; i++)
        {
            if(i>1&&i<m)
            dp[i][j] = a[i][j] + max(dp[i][j-1],max(dp[i-1][j-1],dp[i+1][j-1]));
            if(i==1)
            dp[i][j] = a[i][j] + max(dp[i][j-1],dp[i+1][j-1]);
            if(i==m)
            dp[i][j] = a[i][j] + max(dp[i][j-1],dp[i-1][j-1]);

            ans = max(ans, dp[i][j]);
        }
    }
   cout << ans;
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    enter();
    process();
    return 0;
}